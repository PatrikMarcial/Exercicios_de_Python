'''Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz
quadrada.(curso em video)'''

import math
numero = int(input('Me diga um numero inteiro: '))
raiz = math.sqrt(numero)
raiz2 = float(numero) ** 0.5
print(f'\nO dobro de {numero} é {numero*2}, Seu triplo é {numero*3} e sua raiz quadrada {raiz  or raiz2}\n')


