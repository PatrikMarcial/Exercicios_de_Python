'''Faça um código Python que leia a largura e altura de uma parede (em metros)
e exiba a área da Parede'''

Largura = int(input('Me Diga a sua Largura: '))
Altura = int(input('Me diga sua Altura: '))
Area = Largura * Altura
print(f'\nA Área da largura de {Largura} metros e da Altura de {Altura} metros é igual a {Area} Metros quadrados\n')

